package day10_SwitchAndScanner;

public class SwitchStatement {

    public static void main(String[] args) {
        int a=4;
        switch(a){

            case 1:
                System.out.println("One");
                break;

            case 2:
                System.out.println("Two");
                break;

            default:
                System.out.println("Invalid case");
                break;


        }


    }

}
