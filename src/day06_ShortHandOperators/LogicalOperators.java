package day06_ShortHandOperators;

public class LogicalOperators {

    public static void main(String[] args) {

        /*
        && = And
        || = or
         */

        boolean r1 = 9==9 || "muhtar"=="good";
        System.out.println(r1);

        boolean r2 = 8>5 && 7==7;
        System.out.println(r2);


    }




}
